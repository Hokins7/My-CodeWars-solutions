/**
 * This JavaScript code defines a function 'sumPPG' that takes two NBA player objects and calculates the sum of their Points Per Game (PPG).
 *
 * @param {Object} playerOne - An NBA player object with 'ppg' property.
 * @param {Object} playerTwo - Another NBA player object with 'ppg' property.
 * @returns {number} - The sum of PPG for the two players.
 */
function sumPPG(playerOne, playerTwo) {
    return playerOne.ppg + playerTwo.ppg;
  }
  