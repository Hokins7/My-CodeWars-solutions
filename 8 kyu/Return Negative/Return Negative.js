/**
 * Return a negative version of a given number.
 * @param {number} num - The input number.
 * @returns {number} - The negative version of the input number.
 */
function makeNegative(num) {
    // The `Math.abs()` function is used to obtain the absolute value of `num`.
    // The negative sign is added with a minus (-) in front, effectively turning it into a negative number.
    return -Math.abs(num);
  }
  