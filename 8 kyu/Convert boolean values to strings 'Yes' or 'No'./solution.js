/**
 * This JavaScript code defines a function 'boolToWord' that converts a boolean value to a "Yes" or "No" string.
 *
 * @param {boolean} bool - A boolean value to be converted.
 * @returns {string} - "Yes" if the boolean is true, "No" if the boolean is false.
 */
function boolToWord(bool) {
    return bool ? 'Yes' : 'No';
  }
  