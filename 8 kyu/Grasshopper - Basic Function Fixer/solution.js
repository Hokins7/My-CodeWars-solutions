/**
 * This function, addFive, adds five to the given number and returns the result.
 * @param {number} num - The input number to which five will be added.
 * @returns {number} - The result of adding five to the input number.
 */
function addFive(num) {
    // Adding five to the input number
    var total = num + 5;
    // Returning the result
    return total;
  }
  