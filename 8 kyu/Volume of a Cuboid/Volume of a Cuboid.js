/**
 * Calculate the volume of a cuboid (rectangular box) using its length, width, and height.
 */
class Kata {
  /**
   * Get the volume of the cuboid.
   * @param {number} length - The length of the cuboid.
   * @param {number} width - The width of the cuboid.
   * @param {number} height - The height of the cuboid.
   * @returns {number} - The volume of the cuboid.
   */
  static getVolumeOfCuboid(length, width, height) {
    return length * width * height;
  }
}